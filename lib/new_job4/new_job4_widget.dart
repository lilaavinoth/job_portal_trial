      final payCompensation = FirebaseFirestore.instance
          .collection('users')
          .doc(currentUserUid)
          .collection('incompleteJob')
          .doc('job');

      final newdata = compensationModel(
          payType: _model.dropDownValue,
          min: int.tryParse(_model.minController.text),
          max: int.tryParse(_model.maxController.text),
          minMaxRate: _model.mMDropDownValue,
          amount: int.tryParse(_model.amountController.text),
          amountRate: _model.amountDropDownValue,
          supplementalPay: _model.choiceChipsValues1,
          benefits: _model.choiceChipsValues2,
          timestamp: timestamp,
          progress: 3);

      await payCompensation.update(newdata.toMap());
