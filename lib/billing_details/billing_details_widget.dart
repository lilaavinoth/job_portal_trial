  Future fetchFullJob() async {
    DocumentSnapshot snapshot = await FirebaseFirestore.instance
        .collection('users')
        .doc(currentUserUid)
        .collection('incompleteJob')
        .doc("job")
        .get();

    if (!snapshot.exists) {
      return;
    }

    jobObject = fullJobModel.fromMap(snapshot.data() as Map<String, dynamic>);

    String currentUserid = currentUserUid; // replace with the actual user ID
    String collectionPath = 'users/$currentUserid/incompleteJob';

    await deleteCollection(collectionPath);
  }

  Future deleteCollection(String collectionPath) async {
    CollectionReference collectionRef =
        FirebaseFirestore.instance.collection(collectionPath);

    QuerySnapshot querySnapshot = await collectionRef.get();
    List<QueryDocumentSnapshot> documents = querySnapshot.docs;

    WriteBatch batch = FirebaseFirestore.instance.batch();

    for (QueryDocumentSnapshot document in documents) {
      batch.delete(document.reference);
    }

    await batch.commit();

    await uploadToLiveJob();
  }

  Future uploadToLiveJob() async {
    Timestamp timestamp = Timestamp.now();
    final liveJobPath = FirebaseFirestore.instance
        .collection('liveJobs')
        .doc();

    fullJobModel updated = jobObject.copyWith(
          addressLine_1: _model.address1Controller.text,
          addressLine_2: _model.address2Controller.text,
          townCity: _model.townCityController.text,
          postCode: _model.postCodeController.text,
          price: 0.99,
          postedBy: currentUserUid,
          timestamp: timestamp);


    await liveJobPath.set(updated.toMap());

    context.goNamed('homepage');

  }
